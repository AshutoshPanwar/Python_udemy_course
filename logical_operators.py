#Logical operators are( and, or, not)
    # and operator
        #True and True = True
        #False and True = False
        #True and False = False
        #False and False = False
    # or operator
        #True or True = True
        #False or True = True
        #True or False = True
        #False or False = False

x = True
y = False

print(x and y)  #False
print(x or y)   #True
print(not x)    #False